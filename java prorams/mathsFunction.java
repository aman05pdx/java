public class mathsFunction {
    public static void main(String[] args) {
        // we can use the math function  by math class  as Math.function_name()
        double num;
        num=Math.max(20, 25);
        System.out.println("maximum is: "+num);
        num=Math.sqrt(625);
        System.out.println(num);
        num=Math.pow(2, 3);
        System.out.println(num);
        num=Math.log(8);
        System.out.println(num);
        num=Math.ceil(2.61);
        System.out.println(num);
    }
}
